package com.BasicJava;

public class A_1503_MultiplicationTable {

	public static void main(String[] args) {
		
		
		int j=3;
		
		for(int i=1;i<=20;i++)
		{
			
			System.out.println(j+" * "+i+" = "+(i*j));
			
		}
		
		System.out.println("////REVERSE TABLE/////");
        
		
		int a=2;
		
		for(int b=5;b>=1;b--)
		{
			
			System.out.println(a+" * "+b+" = "+(a*b));
			
		}

		
		

	}

}
